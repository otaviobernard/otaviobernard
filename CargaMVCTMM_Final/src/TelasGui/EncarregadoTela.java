    public void prepararCriar(){
        lblTitulo.setText("Cadastro de Fornecedor");
        
        txtNome.setText("");
        txtNome.setEditable(true);
        txtNome.setEnabled(true);
        
        txtRazaoSocial.setText("");
        txtRazaoSocial.setEditable(true);
        txtRazaoSocial.setEnabled(true);        
        
        txtTelefone.setText("");
        txtTelefone.setEditable(true);
        txtTelefone.setEnabled(true);       
        
        txtEndereco.setText("");
        txtEndereco.setEditable(true);
        txtEndereco.setEnabled(true);     
        
        btnOk.setText("Salvar");
        btnOk.setVisible(true);
        
        btnCancelar.setText("Cancelar");
        btnCancelar.setVisible(true);
        

        
    }
    
    
    public void prepararEditar(Encarregado fornecedor){

        lblTitulo.setText("Alterar Fornecedor");
        
        
        txtNome.setText(fornecedor.getNome());
        txtNome.setEditable(true);
        txtNome.setEnabled(true);
        
        txtRazaoSocial.setText(fornecedor.getCpf());
        txtRazaoSocial.setEditable(true);
        txtRazaoSocial.setEnabled(true);
        
        
        
        txtTelefone.setText(fornecedor.getTelefone());
        txtTelefone.setEditable(true);
        txtTelefone.setEnabled(true);
        
        txtEndereco.setText(fornecedor.getEndereco());
        txtEndereco.setEditable(true);
        txtEndereco.setEnabled(true);

        
        btnOk.setText("Alterar");
        btnOk.setVisible(true);
        
        btnCancelar.setText("Cancelar");
        btnCancelar.setVisible(true);
        


                
    }
    
    
    public void prepararMostra(Encarregado mostrar ){
        lblTitulo.setText("Dados Fornecedor");
        
        txtNome.setText(mostrar.getNome());
        txtNome.setEditable(false);
        txtNome.setEnabled(false);
        
        txtRazaoSocial.setText(mostrar.getCpf());
        txtRazaoSocial.setEditable(false);
        txtRazaoSocial.setEnabled(false);
        
        
        
        txtTelefone.setText(mostrar.getTelefone());
        txtTelefone.setEditable(false);
        txtTelefone.setEnabled(false);
        
        
        txtEndereco.setText(mostrar.getEndereco());
        txtEndereco.setEditable(false);
        txtEndereco.setEnabled(false);     
        
        btnOk.setText("Ok");
        btnOk.setVisible(true);
        
        btnCancelar.setText("Cancelar");
        btnCancelar.setVisible(false);

    }
    

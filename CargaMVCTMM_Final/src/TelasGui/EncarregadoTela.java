    @Override
    public Encarregado criar() {
        this.limpaTela();
        this.prepararCriar();
        
        Encarregado fornecedor = null;

        this.setVisible(true);
        if (retornoOk) {
            this.getScreenObject();
            
        }
        return fornecedor;
        
    }
    
      public Encarregado getScreenObject(){

        Encarregado novo = new Encarregado();
        novo.setNome(txtNome.getText());
        novo.setCpf(txtRazaoSocial.getText());
        novo.setTelefone(txtTelefone.getText());

        novo.setEndereco(txtEndereco.getText());
        
        
        
        return novo;
    }
    
    public Encarregado limpaTela(){
        
        txtNome.setText("");
        txtEndereco.setText("");
        txtRazaoSocial.setText("");
        txtTelefone.setText("");
        
        return null;
    }
    public void preencherTela(Encarregado preencha){
        txtNome.setText(preencha.getNome());
        txtEndereco.setText(preencha.getEndereco());
        txtRazaoSocial.setText(preencha.getCpf());
        txtTelefone.setText(preencha.getTelefone());
    }

    @Override
    public void mostrar(Encarregado type) {
        prepararMostra(type);
        this.setVisible(true);
    }

    @Override
    public Encarregado editar(Encarregado fornecedor) {
        this.prepararEditar(fornecedor);

        this.setVisible(true);
        if(retornoOk){
            fornecedor.setNome(txtNome.getText());
            fornecedor.setCpf(txtRazaoSocial.getText());
            fornecedor.setTelefone(txtTelefone.getText());
            fornecedor.setEndereco(txtEndereco.getText());
        }
        
        
        return fornecedor;
    }

